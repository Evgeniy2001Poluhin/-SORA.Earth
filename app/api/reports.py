@router.post("/compliance.pdf", summary="Generate ESG/CSRD compliance PDF")
def compliance_pdf(project: ProjectInput, lang: str = Query("en", pattern="^(en|ru)$")):
    t = I18N.get(lang, I18N["en"])
    res = _score(project)
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, leftMargin=2*cm, rightMargin=2*cm,
                            topMargin=2*cm, bottomMargin=2*cm, title=t["title"])
    s = getSampleStyleSheet()
    h1 = ParagraphStyle("H1", parent=s["Heading1"], fontName=BOLD_FONT, textColor=colors.HexColor("#0B5FFF"))
    h2 = ParagraphStyle("H2", parent=s["Heading2"], fontName=BOLD_FONT)
    body = ParagraphStyle("B", parent=s["BodyText"], fontName=BASE_FONT)
    story = []
    _lg = _logo_flowable()
    if _lg is not None:
        story.append(_lg); story.append(Spacer(1, 0.3*cm))
    story += [Paragraph(t["title"], h1),
             Paragraph(t["gen"] + ": " + datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"), body),
             Spacer(1, 0.5*cm), Paragraph(t["overview"], h2)]
    proj = [[t["name"], project.name],[t["cat"], project.category],[t["reg"], project.region],
            [t["budget"], "{:,.0f}".format(project.budget)],
            [t["co2"], "{:,.0f}".format(project.co2_reduction)],
            [t["social"], str(project.social_impact)],[t["dur"], str(project.duration_months)]]
    t1 = Table(proj, colWidths=[5*cm, 10*cm])
    t1.setStyle(TableStyle([("GRID",(0,0),(-1,-1),0.3,colors.grey),
                            ("FONTNAME",(0,0),(-1,-1),BASE_FONT),
                            ("FONTNAME",(0,0),(0,-1),BOLD_FONT),
                            ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#EEF3FF"))]))
    story += [t1, Spacer(1, 0.5*cm),
              Paragraph(t["overall"] + ": <b>" + str(res["overall"]) + "/100</b>", h2),
              _esg_chart(res["env"], res["social"], res["governance"]), Spacer(1, 0.3*cm)]
    t2 = Table([[t["pillar"], t["score"]],[t["env"], res["env"]],
                [t["soc"], res["social"]],[t["gov"], res["governance"]]], colWidths=[7*cm,4*cm])
    t2.setStyle(TableStyle([("BACKGROUND",(0,0),(-1,0),colors.HexColor("#0B5FFF")),
                            ("TEXTCOLOR",(0,0),(-1,0),colors.white),
                            ("GRID",(0,0),(-1,-1),0.3,colors.grey),
                            ("FONTNAME",(0,0),(-1,-1),BASE_FONT),
                            ("FONTNAME",(0,0),(-1,0),BOLD_FONT)]))
    story += [t2, Spacer(1, 0.5*cm), Paragraph(t["fw"], h2)]
    rows = [[t["fwh"], t["score"], t["status"]]]
    for k,(sc,st) in res["frameworks"].items():
        rows.append([k, sc, st])
    t3 = Table(rows, colWidths=[7*cm,3*cm,5*cm])
    t3.setStyle(TableStyle([("BACKGROUND",(0,0),(-1,0),colors.HexColor("#0B5FFF")),
                            ("TEXTCOLOR",(0,0),(-1,0),colors.white),
                            ("GRID",(0,0),(-1,-1),0.3,colors.grey),
                            ("FONTNAME",(0,0),(-1,-1),BASE_FONT),
                            ("FONTNAME",(0,0),(-1,0),BOLD_FONT),
                            ("ROWBACKGROUNDS",(0,1),(-1,-1),[colors.white, colors.HexColor("#F5F8FF")])]))
    story += [t3, PageBreak(), Paragraph(t["disc"], h2), Paragraph(t["disc_t"], body)]
    doc.build(story)
    buf.seek(0)
    fname = "compliance_" + project.name.replace(" ","_") + "_" + lang + ".pdf"
    _track_pdf("compliance", lang)
    return StreamingResponse(buf, media_type="application/pdf",
        headers={"Content-Disposition": "attachment; filename=" + fname})

