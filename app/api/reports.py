class ProjectInput(BaseModel):
    name: str
    budget: float = Field(ge=0)
    co2_reduction: float = Field(ge=0)
    social_impact: float = Field(ge=0, le=10)
    duration_months: int = Field(ge=1)
    category: str
    region: str
    description: Optional[str] = None

def _score(p):
    env = min(100.0, p.co2_reduction / 10.0 + 40.0)
    social = p.social_impact * 10.0
    governance = 70.0 + (10.0 if p.budget >= 100000 else 0.0) + (10.0 if p.duration_months >= 12 else 0.0)
    overall = round((env + social + governance) / 3.0, 1)
    es = (env + social) / 2.0
    fw = {
        "EU Taxonomy": (round(env,1), "Aligned" if env>=70 else "Partial"),
        "CSRD (ESRS E1)": (round(env,1), "Compliant" if env>=75 else "Needs work"),
        "SFDR Article 9": (round(es,1), "Eligible" if es>=70 else "Article 8"),
        "GRI Standards": (round(social,1), "Compliant" if social>=60 else "Partial"),
        "TCFD": (round(governance,1), "Compliant" if governance>=70 else "Partial"),
    }
    return {"overall":overall,"env":round(env,1),"social":round(social,1),
            "governance":round(governance,1),"frameworks":fw}

def _esg_chart(env, social, gov, labels=None):
    d = Drawing(400, 180)
    bc = VerticalBarChart()
    bc.x = 50; bc.y = 30; bc.height = 130; bc.width = 320
    bc.data = [[env, social, gov]]
    bc.categoryAxis.labels.fontName = BASE_FONT
    bc.valueAxis.labels.fontName = BASE_FONT
    bc.categoryAxis.categoryNames = labels or ["Environmental","Social","Governance"]
    bc.bars[0].fillColor = colors.HexColor("#0B5FFF")
    bc.valueAxis.valueMin = 0; bc.valueAxis.valueMax = 100; bc.valueAxis.valueStep = 20
    d.add(bc)
    return d


